public class Do_While_Loop {
    public static void main(String[] args) {
        int a = 1;
        do {
            System.out.println(a);
            a++;}
            while(a<=4);


        // print even numbers
        System.out.println("print even numbers: ");
        int i = 0;
        do {
            System.out.println(2*i);
            i++;
        }while (i <=5);



    }
}
