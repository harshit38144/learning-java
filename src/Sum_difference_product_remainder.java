public class Sum_difference_product_remainder {
    public static void main(String[] args) {
        int b = 11,  a = 22;
        int sum = a+b;
        int diff = b-a;
        int product = a*b;
        int remainder = b%a;
        System.out.println("sum of these two numbers is "+sum);
        System.out.println("difference of these two numbers is "+diff);
        System.out.println("product of these two numbers is "+product);
        System.out.println("remainder of these two numbers is "+remainder);


    }
}
