public class For_Loop {
    public static void main(String[] args) {
        for (int i = 2; i <= 5; i++) {
            System.out.println(i);
        }



        //print odd numbers
        System.out.println("print odd numbers: ");
        for (int i = 0; i <= 5; i++){
            System.out.println((2*i)+1);
        }

    }
}
